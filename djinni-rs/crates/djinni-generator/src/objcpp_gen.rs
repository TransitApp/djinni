fn fmt_float(d: f64) -> String {
    let s = d.to_string();
    if s.contains('.') || s.contains('E') || s.contains('e') {
        s
    } else {
        format!("{}.0", s)
    }
}

// --- ObjcppRefs ---

struct ObjcppRefs {
    priv_header: BTreeSet<String>,
    body: BTreeSet<String>,
}

impl ObjcppRefs {
    fn new() -> Self {
        Self {
            priv_header: BTreeSet::new(),
            body: BTreeSet::new(),
        }
    }

    fn find_type_ref(&mut self, ty: &TypeRef, objcpp_marshal: &ObjcppMarshal) {
        if let Some(resolved) = &ty.resolved {
            self.find_mexpr(resolved, objcpp_marshal);
        }
    }

    fn find_mexpr(&mut self, tm: &MExpr, objcpp_marshal: &ObjcppMarshal) {
        for arg in &tm.args {
            self.find_mexpr(arg, objcpp_marshal);
        }
        self.find_meta(&tm.base, objcpp_marshal);
    }

    fn find_meta(&mut self, m: &Meta, objcpp_marshal: &ObjcppMarshal) {
        for r in objcpp_marshal.references(m) {
            match r {
                SymbolReference::ImportRef(arg) => {
                    self.body.insert(format!("#import {}", arg));
                }
                _ => {}
            }
        }
    }
}

// --- write ObjC++ file ---

fn write_objcpp_file<F>(
    out_files: &mut Vec<std::path::PathBuf>,
    written_files: &mut std::collections::HashMap<String, String>,
    folder: &std::path::Path,
    file_name: &str,
    origin: &str,
    refs: &BTreeSet<String>,
    f: F,
) where
    F: FnOnce(&mut IndentWriter),
{
    create_file_from_parts(out_files, written_files, folder, file_name, |w| {
        w.wl("// AUTOGENERATED FILE - DO NOT MODIFY!");
        w.wl(&format!(
            "// This file was generated by Djinni from {}",
            origin
        ));
        w.wl_empty();
        if !refs.is_empty() {
            let mut included = BTreeSet::new();
            for s in refs {
                let include = if s.starts_with('!') { &s[1..] } else { s.as_str() };
                if included.insert(include.to_string()) {
                    w.wl(include);
                }
            }
            w.wl_empty();
        }
        f(w);
    });
}

fn arc_assert(w: &mut IndentWriter) {
    w.wl(&format!(
        "static_assert(__has_feature(objc_arc), {});",
        q("Djinni requires ARC to be enabled for this file")
    ));
}

// --- aligned ObjC call (duplicated from objc_gen for independence) ---

fn write_aligned_objc_call<F>(
    w: &mut IndentWriter,
    call: &str,
    params: &[Field],
    end: &str,
    f: F,
) where
    F: Fn(&Field) -> (String, String),
{
    w.w(call);
    let mut first = true;
    for p in params {
        let (name, value) = f(p);
        if first {
            first = false;
        } else {
            w.wl_empty();
            let padding = if call.len() > name.len() {
                call.len() - name.len()
            } else {
                0
            };
            w.w(&" ".repeat(padding));
            w.w(&name);
        }
        w.w(&format!(":{}", value));
    }
    w.w(end);
}

fn nn_check(spec: &Spec, expr: &str) -> String {
    match &spec.cpp_nn_check_expression {
        Some(check) => format!("{}({})", check, expr),
        None => expr.to_string(),
    }
}

fn header_name(spec: &Spec, ident: &str) -> String {
    let id_objc_ty = format!(
        "{}{}",
        spec.objc_type_prefix,
        (spec.objc_ident_style.ty)(ident)
    );
    format!("{}.{}", id_objc_ty, spec.objc_header_ext)
}

fn private_body_name(spec: &Spec, ident: &str) -> String {
    let id_objc_ty = format!(
        "{}{}",
        spec.objc_type_prefix,
        (spec.objc_ident_style.ty)(ident)
    );
    format!("{}+Private.{}", id_objc_ty, spec.objcpp_ext)
}

#[allow(dead_code)]
fn maybe_move(_spec: &Spec, expr: &str, ty: &TypeRef) -> String {
    match ty.resolved.as_ref() {
        Some(tm) if move_only(tm) => format!("std::move({})", expr),
        _ => expr.to_string(),
    }
}

fn maybe_move_mexpr(_spec: &Spec, expr: &str, tm: &MExpr) -> String {
    if move_only(tm) {
        format!("std::move({})", expr)
    } else {
        expr.to_string()
    }
}

fn move_only(tm: &MExpr) -> bool {
    match &tm.base {
        Meta::MDef(d) => {
            if let TypeDef::Record(r) = &d.body {
                r.fields.iter().any(|f| {
                    match f.ty.resolved.as_ref() {
                        Some(ftm) => move_only(ftm),
                        None => false,
                    }
                })
            } else {
                false
            }
        }
        Meta::MExtern(e) => match e.def_type {
            DefType::Record => e.cpp.move_only,
            _ => false,
        },
        Meta::MOptional => {
            if let Some(first) = tm.args.first() {
                move_only(first)
            } else {
                false
            }
        }
        _ => false,
    }
}

// --- Main entry point ---

pub fn generate_objcpp(ctx: &mut GeneratorContext, idl: &[TypeDecl]) {
    let objcpp_out = match &ctx.spec.objcpp_out_folder {
        Some(f) => f.clone(),
        None => return,
    };

    let intern_decls: Vec<_> = idl
        .iter()
        .filter_map(|td| {
            if let TypeDecl::Intern {
                ident,
                params,
                body,
                doc,
                origin,
            } = td
            {
                Some((
                    ident.clone(),
                    params.clone(),
                    body.clone(),
                    doc.clone(),
                    origin.clone(),
                ))
            } else {
                None
            }
        })
        .collect();

    for (ident, params, body, doc, origin) in &intern_decls {
        match body {
            TypeDef::Enum(e) => {
                generate_enum(ctx, &objcpp_out, origin, ident, doc, e);
            }
            TypeDef::Record(r) => {
                generate_record(ctx, &objcpp_out, origin, ident, doc, params, r, idl);
            }
            TypeDef::Interface(i) => {
                generate_interface(ctx, &objcpp_out, origin, ident, doc, params, i, idl);
            }
            TypeDef::ProtobufMessage(_) => {}
        }
    }
}

// --- Enum ---

fn generate_enum(
    ctx: &mut GeneratorContext,
    objcpp_out: &std::path::Path,
    origin: &str,
    ident: &Ident,
    _doc: &Doc,
    _e: &Enum,
) {
    let (spec, out_files, written_files) = ctx.split_borrow();
    let objcpp_marshal = ObjcppMarshal::new(spec);

    let mut imports = BTreeSet::new();
    imports.insert(format!(
        "#import {}",
        q(&format!("{}DJIMarshal+Private.h", spec.objc_base_lib_include_prefix))
    ));
    imports.insert(format!(
        "!#include {}",
        q(&format!(
            "{}{}.{}",
            spec.objcpp_include_cpp_prefix,
            (spec.cpp_file_ident_style)(&ident.name),
            spec.cpp_header_ext
        ))
    ));

    let file_name = objcpp_marshal.private_header_name(&ident.name);
    write_objcpp_file(out_files, written_files, objcpp_out, &file_name, origin, &imports, |_w| {});
}

// --- Interface ---

fn generate_interface(
    ctx: &mut GeneratorContext,
    objcpp_out: &std::path::Path,
    origin: &str,
    ident: &Ident,
    _doc: &Doc,
    _params: &[TypeParam],
    i: &Interface,
    idl: &[TypeDecl],
) {
    let (spec, out_files, written_files) = ctx.split_borrow();
    let objc_marshal = ObjcMarshal::new(spec);
    let objcpp_marshal = ObjcppMarshal::new(spec);
    let cpp_marshal = CppMarshal::new(spec);
    let id_objc = &spec.objc_ident_style;
    let id_cpp = &spec.cpp_ident_style;

    let mut refs = ObjcppRefs::new();
    for m in &i.methods {
        for p in &m.params {
            refs.find_type_ref(&p.ty, &objcpp_marshal);
        }
        if let Some(ret) = &m.ret {
            refs.find_type_ref(ret, &objcpp_marshal);
        }
    }
    for c in &i.consts {
        refs.find_type_ref(&c.ty, &objcpp_marshal);
    }

    let self_name = objc_marshal.typename_from_name(&ident.name, &TypeDef::Interface(i.clone()));
    let cpp_self = cpp_marshal.fq_typename_from_name(&ident.name, &TypeDef::Interface(i.clone()));

    refs.priv_header.insert("#include <memory>".to_string());
    refs.priv_header.insert(format!(
        "!#include {}",
        q(&format!(
            "{}{}.{}",
            spec.objcpp_include_cpp_prefix,
            (spec.cpp_file_ident_style)(&ident.name),
            spec.cpp_header_ext
        ))
    ));
    refs.body.insert(format!(
        "!#import {}",
        q(&format!(
            "{}{}",
            spec.objcpp_include_objc_prefix,
            header_name(spec, &ident.name)
        ))
    ));
    refs.body.insert(format!(
        "!#import {}",
        q(&format!(
            "{}{}",
            spec.objcpp_include_prefix,
            objcpp_marshal.private_header_name(&ident.name)
        ))
    ));

    if let Some(ref nn_hdr) = spec.cpp_nn_header {
        refs.priv_header.insert(format!("#include {}", nn_hdr));
    }

    let helper_class = objcpp_marshal.helper_class_name(&ident.name);

    // Generate +Private.h
    let priv_header_name = objcpp_marshal.private_header_name(&ident.name);
    write_objcpp_file(
        out_files,
        written_files,
        objcpp_out,
        &priv_header_name,
        origin,
        &refs.priv_header,
        |w| {
            arc_assert(w);
            w.wl_empty();
            if use_protocol(&i.ext, spec) {
                w.wl(&format!("@protocol {};", self_name));
            } else {
                w.wl(&format!("@class {};", self_name));
            }
            w.wl_empty();
            wrap_namespace(w, &spec.objcpp_namespace, |w| {
                w.wl(&format!("class {}", helper_class));
                w.braced_semi(|w| {
                    w.wl_outdent("public:");
                    match &spec.cpp_nn_type {
                        Some(nn_ptr) => {
                            w.wl(&format!("using CppType = {}<{}>;", nn_ptr, cpp_self));
                            w.wl(&format!("using CppOptType = std::shared_ptr<{}>;", cpp_self));
                        }
                        None => {
                            w.wl(&format!("using CppType = std::shared_ptr<{}>;", cpp_self));
                            w.wl(&format!("using CppOptType = std::shared_ptr<{}>;", cpp_self));
                        }
                    }
                    let objc_type = if use_protocol(&i.ext, spec) {
                        format!("id<{}>", self_name)
                    } else {
                        format!("{}*", self_name)
                    };
                    w.wl(&format!("using ObjcType = {};", objc_type));
                    w.wl_empty();
                    w.wl(&format!("using Boxed = {};", helper_class));
                    w.wl_empty();
                    w.wl("static CppType toCpp(ObjcType objc);");
                    w.wl("static ObjcType fromCppOpt(const CppOptType& cpp);");
                    w.wl("static ObjcType fromCpp(const CppType& cpp) { return fromCppOpt(cpp); }");
                    w.wl_empty();
                    w.wl_outdent("private:");
                    w.wl("class ObjcProxy;");
                });
            });
            w.wl_empty();
        },
    );

    let has_static_method = i.methods.iter().any(|m| m.is_static);

    // Build body refs
    if let Some(ref x) = spec.objcpp_function_prologue_file {
        refs.body.insert(format!("#include {}", q(x)));
    }
    if i.ext.cpp {
        refs.body.insert(format!(
            "#import {}",
            q(&format!("{}DJICppWrapperCache+Private.h", spec.objc_base_lib_include_prefix))
        ));
        refs.body.insert("#include <utility>".to_string());
        refs.body.insert(format!(
            "#import {}",
            q(&format!("{}DJIError.h", spec.objc_base_lib_include_prefix))
        ));
        refs.body.insert("#include <exception>".to_string());

        if spec.objc_gen_protocol && has_static_method {
            refs.body.insert(format!(
                "#import {}",
                q(&format!(
                    "{}{}",
                    spec.objc_include_prefix,
                    objc_marshal.impl_header_name(&ident.name)
                ))
            ));
        }
    }
    // Match Scala behavior: condition is `!cppNnType.isEmpty || !cppNnCheckExpression.nonEmpty`
    // which evaluates to true when cppNnCheckExpression is None (the default)
    if spec.cpp_nn_type.is_some() || spec.cpp_nn_check_expression.is_none() {
        refs.body.insert("#include <stdexcept>".to_string());
    }
    if i.ext.objc {
        refs.body.insert(format!(
            "#import {}",
            q(&format!("{}DJIObjcWrapperCache+Private.h", spec.objc_base_lib_include_prefix))
        ));
    }
    if !i.ext.cpp && !i.ext.objc {
        refs.body.insert(format!(
            "#import {}",
            q(&format!("{}DJIError.h", spec.objc_base_lib_include_prefix))
        ));
    }

    let write_objc_func_decl = |w: &mut IndentWriter, method: &Method| {
        let label = if method.is_static { "+" } else { "-" };
        let ret = objc_marshal.fq_return_type(method.ret.as_ref());
        let decl = format!(
            "{} ({}){}", label, ret, (id_objc.method)(&method.ident.name)
        );
        write_aligned_objc_call(
            w,
            &decl,
            &method.params,
            "",
            |p| {
                (
                    (id_objc.field)(&p.ident.name),
                    format!(
                        "({}){}",
                        objc_marshal.param_type(&p.ty),
                        (id_objc.local)(&p.ident.name)
                    ),
                )
            },
        );
    };

    // Generate +Private.mm
    let body_name = private_body_name(spec, &ident.name);
    write_objcpp_file(
        out_files,
        written_files,
        objcpp_out,
        &body_name,
        origin,
        &refs.body,
        |w| {
            arc_assert(w);

            let objc_self = if i.ext.objc && i.ext.cpp {
                format!("{}CppProxy", self_name)
            } else {
                self_name.clone()
            };

            if i.ext.cpp {
                w.wl_empty();
                if i.ext.objc {
                    w.wl(&format!("@interface {} : NSObject<{}>", objc_self, self_name));
                } else if spec.objc_gen_protocol && !has_static_method {
                    w.wl(&format!("@interface {} : NSObject<{}>", objc_self, self_name));
                } else {
                    w.wl(&format!("@interface {} ()", objc_self));
                }
                w.wl_empty();
                w.wl(&format!(
                    "- (id)initWithCpp:(const std::shared_ptr<{}>&)cppRef;",
                    cpp_self
                ));
                w.wl_empty();
                w.wl("@end");
                w.wl_empty();
                w.wl(&format!("@implementation {} {{", objc_self));
                w.wl(&format!(
                    "    ::djinni::CppProxyCache::Handle<std::shared_ptr<{}>> _cppRefHandle;",
                    cpp_self
                ));
                w.wl("}");
                w.wl_empty();
                w.wl(&format!(
                    "- (id)initWithCpp:(const std::shared_ptr<{}>&)cppRef",
                    cpp_self
                ));
                w.braced(|w| {
                    w.w("if (self = [super init])");
                    w.braced(|w| {
                        w.wl("_cppRefHandle.assign(cppRef);");
                    });
                    w.wl("return self;");
                });

                // Methods
                for m in i.methods.iter().filter(|m| !m.is_static || m.lang.objc) {
                    w.wl_empty();
                    write_objc_func_decl(w, m);
                    w.braced(|w| {
                        let write_body = |w: &mut IndentWriter| {
                            if let Some(ref _x) = spec.objcpp_function_prologue_file {
                                w.wl(&format!(
                                    "DJINNI_FUNCTION_PROLOGUE(\"{}.{}\");",
                                    ident.name, m.ident.name
                                ));
                            }
                            // Non-null parameter checks
                            for p in &m.params {
                                let tm = match p.ty.resolved.as_ref() {
                                    Some(tm) => tm,
                                    None => continue,
                                };
                                if is_interface(tm) && spec.cpp_nn_check_expression.is_some() {
                                    let param_name = (id_objc.local)(&p.ident.name);
                                    w.w(&format!("if ({} == nil)", param_name));
                                    w.braced(|w| {
                                        w.wl(&format!(
                                            "throw std::invalid_argument(\"Got unexpected null parameter '{}'\");",
                                            param_name
                                        ));
                                    });
                                }
                            }

                            let ret_prefix = if m.ret.is_some() {
                                "auto objcpp_result_ = "
                            } else {
                                ""
                            };
                            let call_target = if !m.is_static {
                                "_cppRefHandle.get()->"
                            } else {
                                &format!("{}::", cpp_self)
                            };
                            let call_start = format!(
                                "{}{}{}(",
                                ret_prefix,
                                call_target,
                                (id_cpp.method)(&m.ident.name)
                            );
                            write_aligned_call(
                                w,
                                &call_start,
                                &m.params,
                                ",",
                                ")",
                                |p| {
                                    let tm = match p.ty.resolved.as_ref() {
                                        Some(tm) => tm,
                                        None => return (id_objc.local)(&p.ident.name),
                                    };
                                    objcpp_marshal.to_cpp_mexpr(tm, &(id_objc.local)(&p.ident.name))
                                },
                            );
                            w.wl(";");

                            if let Some(ref ret) = m.ret {
                                if let Some(ret_tm) = ret.resolved.as_ref() {
                                    let moved = maybe_move_mexpr(spec, "objcpp_result_", ret_tm);
                                    w.wl(&format!(
                                        "return {};",
                                        objcpp_marshal.from_cpp_mexpr(ret_tm, &moved)
                                    ));
                                }
                            }
                        };

                        if spec.objcpp_disable_exception_translation {
                            write_body(w);
                        } else {
                            w.w("try");
                            w.braced_end(" DJINNI_TRANSLATE_EXCEPTIONS()", write_body);
                        }
                    });
                }

                // Constants implemented as methods
                if !i.consts.is_empty() {
                    w.wl_empty();
                    generate_objc_constants_bridge(
                        w, &i.consts, &self_name, &objc_marshal, spec, idl,
                    );
                }
            }

            // ObjC proxy class
            if i.ext.objc {
                w.wl_empty();
                wrap_namespace(w, &spec.objcpp_namespace, |w| {
                    w.wl(&format!("class {}::ObjcProxy final", helper_class));
                    w.wl(&format!(": public {}", cpp_self));
                    w.wl(", private ::djinni::ObjcProxyBase<ObjcType>");
                    w.braced_semi(|w| {
                        w.wl(&format!(
                            "friend class {};",
                            objcpp_marshal.helper_class_with_ns(&ident.name)
                        ));
                        w.wl_outdent("public:");
                        w.wl("using ObjcProxyBase::ObjcProxyBase;");
                        for m in &i.methods {
                            let ret = cpp_marshal.fq_return_type(m.ret.as_ref());
                            let params_str: Vec<String> = m
                                .params
                                .iter()
                                .filter_map(|p| {
                                    let tm = p.ty.resolved.as_ref()?;
                                    Some(format!(
                                        "{} c_{}",
                                        cpp_marshal.fq_param_type(tm),
                                        (id_cpp.local)(&p.ident.name)
                                    ))
                                })
                                .collect();
                            w.wl(&format!(
                                "{} {}({}) override",
                                ret,
                                (id_cpp.method)(&m.ident.name),
                                params_str.join(", ")
                            ));
                            w.braced(|w| {
                                if let Some(ref _x) = spec.objcpp_function_prologue_file {
                                    w.wl(&format!(
                                        "DJINNI_FUNCTION_PROLOGUE(\"{}.{}\");",
                                        ident.name, m.ident.name
                                    ));
                                }
                                w.w("@autoreleasepool");
                                w.braced(|w| {
                                    let ret_prefix = if m.ret.is_some() {
                                        "auto objcpp_result_ = "
                                    } else {
                                        ""
                                    };
                                    let call = format!(
                                        "{}[djinni_private_get_proxied_objc_object() {}",
                                        ret_prefix,
                                        (id_objc.method)(&m.ident.name)
                                    );
                                    write_aligned_objc_call(w, &call, &m.params, "]", |p| {
                                        let tm = match p.ty.resolved.as_ref() {
                                            Some(tm) => tm,
                                            None => return (
                                                (id_objc.field)(&p.ident.name),
                                                format!("c_{}", (id_cpp.local)(&p.ident.name)),
                                            ),
                                        };
                                        let cpp_local = format!("c_{}", (id_cpp.local)(&p.ident.name));
                                        let moved = maybe_move_mexpr(spec, &cpp_local, tm);
                                        (
                                            (id_objc.field)(&p.ident.name),
                                            format!("({})", objcpp_marshal.from_cpp_mexpr(tm, &moved)),
                                        )
                                    });
                                    w.wl(";");

                                    if let Some(ref ty) = m.ret {
                                        if let Some(ret_tm) = ty.resolved.as_ref() {
                                            if spec.cpp_nn_check_expression.is_some()
                                                && is_interface(ret_tm)
                                            {
                                                w.w("if (objcpp_result_ == nil)");
                                                w.braced(|w| {
                                                    w.wl("throw std::invalid_argument(\"Got unexpected null return value\");");
                                                });
                                            }
                                            w.wl(&format!(
                                                "return {};",
                                                objcpp_marshal.to_cpp_mexpr(ret_tm, "objcpp_result_")
                                            ));
                                        }
                                    }
                                });
                            });
                        }
                    });
                });
            }

            // toCpp / fromCppOpt
            w.wl_empty();
            wrap_namespace(w, &spec.objcpp_namespace, |w| {
                // toCpp
                w.wl(&format!(
                    "auto {}::toCpp(ObjcType objc) -> CppType",
                    helper_class
                ));
                w.braced(|w| {
                    w.w("if (!objc)");
                    w.braced(|w| {
                        if spec.cpp_nn_type.is_none() {
                            w.wl("return nullptr;");
                        } else {
                            w.wl(&format!(
                                "throw std::invalid_argument(\"{}::toCpp requires non-nil object\");",
                                helper_class
                            ));
                        }
                    });
                    if i.ext.cpp && !i.ext.objc {
                        if !spec.objc_gen_protocol {
                            w.wl(&format!(
                                "return {};",
                                nn_check(spec, "objc->_cppRefHandle.get()")
                            ));
                        } else {
                            let get_proxy = format!("(({}*)objc)->_cppRefHandle.get()", objc_self);
                            w.wl(&format!("return {};", nn_check(spec, &get_proxy)));
                        }
                    } else if i.ext.cpp || i.ext.objc {
                        if i.ext.cpp {
                            w.w(&format!(
                                "if ([(id)objc isKindOfClass:[{} class]])",
                                objc_self
                            ));
                            w.braced(|w| {
                                let get_proxy =
                                    format!("(({}*)objc)->_cppRefHandle.get()", objc_self);
                                w.wl(&format!("return {};", nn_check(spec, &get_proxy)));
                            });
                        }
                        let get_proxy = "::djinni::get_objc_proxy<ObjcProxy>(objc)";
                        w.wl(&format!("return {};", nn_check(spec, get_proxy)));
                    } else {
                        w.wl(
                            "DJINNI_UNIMPLEMENTED(@\"Interface not implementable in any language.\");",
                        );
                    }
                });
                w.wl_empty();

                // fromCppOpt
                w.wl(&format!(
                    "auto {}::fromCppOpt(const CppOptType& cpp) -> ObjcType",
                    helper_class
                ));
                w.braced(|w| {
                    w.w("if (!cpp)");
                    w.braced(|w| {
                        w.wl("return nil;");
                    });
                    if i.ext.objc && !i.ext.cpp {
                        w.wl(
                            "return dynamic_cast<ObjcProxy&>(*cpp).djinni_private_get_proxied_objc_object();",
                        );
                    } else if i.ext.objc || i.ext.cpp {
                        if i.ext.objc {
                            w.w("if (auto cppPtr = dynamic_cast<ObjcProxy*>(cpp.get()))");
                            w.braced(|w| {
                                w.wl("return cppPtr->djinni_private_get_proxied_objc_object();");
                            });
                        }
                        w.wl(&format!(
                            "return ::djinni::get_cpp_proxy<{}>(cpp);",
                            objc_self
                        ));
                    } else {
                        w.wl(
                            "DJINNI_UNIMPLEMENTED(@\"Interface not implementable in any language.\");",
                        );
                    }
                });
            });

            if i.ext.cpp {
                w.wl_empty();
                w.wl("@end");
            }
        },
    );
}

fn generate_objc_constants_bridge(
    w: &mut IndentWriter,
    consts: &[Const],
    self_name: &str,
    marshal: &ObjcMarshal,
    spec: &Spec,
    idl: &[TypeDecl],
) {
    // Only generate ConstMethod type constants here
    for c in consts.iter().filter(|c| !marshal.can_be_const_variable(c)) {
        let tm = match c.ty.resolved.as_ref() {
            Some(tm) => tm,
            None => continue,
        };
        let nullability = marshal
            .nullability(tm)
            .map(|n| format!(" __{}", n))
            .unwrap_or_default();
        let ret = format!("{}{}", marshal.fq_field_type(tm), nullability);
        let decl = format!(
            "+ ({}){}",
            ret,
            (spec.objc_ident_style.method)(&c.ident.name)
        );
        write_aligned_objc_call(w, &decl, &[], "", |_| (String::new(), String::new()));
        w.wl_empty();
        w.braced(|w| {
            let static_var = format!("s_{}", (spec.objc_ident_style.method)(&c.ident.name));
            w.w(&format!(
                "static {} const {} = ",
                marshal.fq_field_type(tm),
                static_var
            ));
            // For constants that are ConstMethod, we just write the value
            // (this matches the Scala behavior in BaseObjcGenerator)
            write_objc_const_value_bridge(w, &c.ty, &c.value, self_name, marshal, spec, idl);
            w.wl(";");
            w.wl(&format!("return {};", static_var));
        });
        w.wl_empty();
    }
}

fn find_resolved_record<'a>(idl: &'a [TypeDecl], name: &str) -> Option<&'a Record> {
    idl.iter().find_map(|td| {
        if td.ident().name == name {
            if let TypeDef::Record(r) = td.body() {
                Some(r)
            } else {
                None
            }
        } else {
            None
        }
    })
}

fn write_objc_const_value_bridge(
    w: &mut IndentWriter,
    ty: &TypeRef,
    v: &ConstValue,
    self_name: &str,
    marshal: &ObjcMarshal,
    spec: &Spec,
    idl: &[TypeDecl],
) {
    let tm = match ty.resolved.as_ref() {
        Some(tm) => tm,
        None => return,
    };
    let (_, need_ref) = marshal.to_objc_type(tm, false);
    let boxed_prefix = if need_ref { "@" } else { "" };

    match v {
        ConstValue::Int(l) => { w.w(&format!("{}{}", boxed_prefix, l)); }
        ConstValue::Float(d) => {
            let field_type = marshal.field_type_mexpr(tm);
            if field_type == "float" {
                w.w(&format!("{}{}f", boxed_prefix, fmt_float(*d)));
            } else {
                w.w(&format!("{}{}", boxed_prefix, fmt_float(*d)));
            }
        }
        ConstValue::Bool(b) => {
            w.w(&format!("{}{}", boxed_prefix, if *b { "YES" } else { "NO" }));
        }
        ConstValue::String(s) => { w.w(&format!("@{}", s)); }
        ConstValue::EnumValue { ty: _, value } => {
            let type_name = marshal.typename_from_mexpr(tm);
            w.w(&format!(
                "{}{}",
                type_name,
                (spec.objc_ident_style.enum_)(value)
            ));
        }
        ConstValue::ConstRef(name) => {
            w.w(&format!("{}{}", self_name, (spec.objc_ident_style.const_)(name)));
        }
        ConstValue::Composite(fields) => {
            // Record constant — use find_resolved_record for resolved field types
            if let Meta::MDef(d) = &tm.base {
                if let Some(record) = find_resolved_record(idl, &d.name) {
                    let type_name = marshal.typename_from_mexpr(tm);
                    let head = &record.fields[0];
                    w.w(&format!(
                        "[[{} alloc] initWith{}:",
                        type_name,
                        camel_upper(&head.ident.name)
                    ));
                    if let Some((_, val)) = fields.iter().find(|(k, _)| *k == head.ident.name) {
                        write_objc_const_value_bridge(w, &head.ty, val, self_name, marshal, spec, idl);
                    }
                    w.nested_n(2, |w| {
                        for f in record.fields.iter().skip(1) {
                            w.wl_empty();
                            w.w(&format!(
                                "{}:",
                                (spec.objc_ident_style.field)(&f.ident.name)
                            ));
                            if let Some((_, val)) =
                                fields.iter().find(|(k, _)| *k == f.ident.name)
                            {
                                write_objc_const_value_bridge(
                                    w, &f.ty, val, self_name, marshal, spec, idl,
                                );
                            }
                        }
                    });
                    w.w("]");
                }
            }
        }
    }
}

