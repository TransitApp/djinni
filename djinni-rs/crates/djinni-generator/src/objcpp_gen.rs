fn generate_record(
    ctx: &mut GeneratorContext,
    objcpp_out: &std::path::Path,
    origin: &str,
    ident: &Ident,
    _doc: &Doc,
    _params: &[TypeParam],
    r: &Record,
    idl: &[TypeDecl],
) {
    let (spec, out_files, written_files) = ctx.split_borrow();
    let objc_marshal = ObjcMarshal::new(spec);
    let objcpp_marshal = ObjcppMarshal::new(spec);
    let cpp_marshal = CppMarshal::new(spec);
    let id_objc = &spec.objc_ident_style;
    let id_cpp = &spec.cpp_ident_style;

    let super_record = get_super_record(idl, r);
    let (super_fields, first_initializer_arg) = match &super_record {
        None => {
            let first_arg = if r.fields.is_empty() {
                String::new()
            } else {
                camel_upper(&format!("with_{}", r.fields[0].ident.name))
            };
            (vec![], first_arg)
        }
        Some(sr) => {
            let first_arg = if sr.fields.is_empty() {
                String::new()
            } else {
                camel_upper(&format!("with_{}", sr.fields[0].ident.name))
            };
            (sr.fields.clone(), first_arg)
        }
    };

    let fields: Vec<Field> = [super_fields.clone(), r.fields.clone()].concat();

    let mut refs = ObjcppRefs::new();
    for c in &r.consts {
        refs.find_type_ref(&c.ty, &objcpp_marshal);
    }
    for f in &fields {
        refs.find_type_ref(&f.ty, &objcpp_marshal);
    }

    let objc_name = if r.ext.objc {
        format!("{}_base", ident.name)
    } else {
        ident.name.clone()
    };
    let no_base_self = objc_marshal.typename_from_name(&ident.name, &TypeDef::Record(r.clone()));
    let cpp_self = cpp_marshal.fq_typename_from_name(&ident.name, &TypeDef::Record(r.clone()));

    refs.priv_header.insert(format!(
        "!#import {}",
        q(&format!(
            "{}{}",
            if r.ext.objc {
                &spec.objc_extended_record_include_prefix
            } else {
                &spec.objcpp_include_objc_prefix
            },
            header_name(spec, &ident.name)
        ))
    ));
    refs.priv_header.insert(format!(
        "!#include {}",
        q(&format!(
            "{}{}.{}",
            if r.ext.cpp {
                &spec.cpp_extended_record_include_prefix
            } else {
                &spec.objcpp_include_cpp_prefix
            },
            (spec.cpp_file_ident_style)(&ident.name),
            spec.cpp_header_ext
        ))
    ));

    refs.body.insert("#include <cassert>".to_string());
    refs.body.insert(format!(
        "!#import {}",
        q(&format!(
            "{}{}",
            spec.objcpp_include_prefix,
            objcpp_marshal.private_header_name(&objc_name)
        ))
    ));

    let helper_class = objcpp_marshal.helper_class_name(&ident.name);

    // Generate +Private.h
    let priv_header_name = objcpp_marshal.private_header_name(&objc_name);
    write_objcpp_file(
        out_files,
        written_files,
        objcpp_out,
        &priv_header_name,
        origin,
        &refs.priv_header,
        |w| {
            arc_assert(w);
            w.wl_empty();
            w.wl(&format!("@class {};", no_base_self));
            w.wl_empty();
            wrap_namespace(w, &spec.objcpp_namespace, |w| {
                w.wl(&format!("struct {}", helper_class));
                w.braced_semi(|w| {
                    w.wl(&format!("using CppType = {};", cpp_self));
                    w.wl(&format!("using ObjcType = {}*;", no_base_self));
                    w.wl_empty();
                    w.wl(&format!("using Boxed = {};", helper_class));
                    w.wl_empty();
                    w.wl("static CppType toCpp(ObjcType objc);");
                    w.wl("static ObjcType fromCpp(const CppType& cpp);");
                });
            });
        },
    );

    // Generate +Private.mm
    let body_name = private_body_name(spec, &objc_name);
    write_objcpp_file(
        out_files,
        written_files,
        objcpp_out,
        &body_name,
        origin,
        &refs.body,
        |w| {
            wrap_namespace(w, &spec.objcpp_namespace, |w| {
                // toCpp
                w.wl(&format!(
                    "auto {}::toCpp(ObjcType obj) -> CppType",
                    helper_class
                ));
                w.braced(|w| {
                    w.wl("assert(obj);");
                    if fields.is_empty() {
                        w.wl("(void)obj; // Suppress warnings in relase builds for empty records");
                    }
                    w.wl(&format!("{} model;", cpp_self));
                    for f in &fields {
                        let tm = match f.ty.resolved.as_ref() {
                            Some(tm) => tm,
                            None => continue,
                        };
                        w.wl(&format!(
                            "model.{} = {};",
                            (spec.java_ident_style.field)(&f.ident.name),
                            objcpp_marshal.to_cpp_mexpr(
                                tm,
                                &format!("obj.{}", (id_objc.field)(&f.ident.name))
                            )
                        ));
                    }
                    w.wl("return model;");
                });
                w.wl_empty();

                // fromCpp
                w.wl(&format!(
                    "auto {}::fromCpp(const CppType& cpp) -> ObjcType",
                    helper_class
                ));
                w.braced(|w| {
                    if fields.is_empty() {
                        w.wl("(void)cpp; // Suppress warnings in relase builds for empty records");
                    }
                    let call = format!(
                        "return [[{} alloc] init{}",
                        no_base_self, first_initializer_arg
                    );
                    write_aligned_objc_call(w, &call, &fields, "]", |f| {
                        let tm = match f.ty.resolved.as_ref() {
                            Some(tm) => tm,
                            None => return (
                                (id_objc.field)(&f.ident.name),
                                format!("cpp.{}", (id_cpp.field)(&f.ident.name)),
                            ),
                        };
                        (
                            (id_objc.field)(&f.ident.name),
                            format!(
                                "({})",
                                objcpp_marshal.from_cpp_mexpr(
                                    tm,
                                    &format!("cpp.{}", (id_cpp.field)(&f.ident.name))
                                )
                            ),
                        )
                    });
                    w.wl(";");
                });
            });
        },
    );
}
